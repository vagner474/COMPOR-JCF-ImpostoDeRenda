package br.ufcg.ppgcc.compor.ir;

public class Resultado {

	private double impostoDevido;
	private double impostoPago;
	private double impostoAPagar;

	public double getImpostoDevido() {
		return impostoDevido;
	}

	public void setImpostoDevido(double impostoDevido) {
		this.impostoDevido = impostoDevido;
	}

	public double getImpostoPago() {
		return impostoPago;
	}

	public void setImpostoPago(double impostoPago) {
		this.impostoPago = impostoPago;
	}

	public double getImpostoAPagar() {
		return impostoAPagar;
	}

	public void setImpostoAPagar(double impostoAPagar) {
		this.impostoAPagar = impostoAPagar;
	}

}
